class Piece1 {

    contructor(description, name, author, age, classification) {
        this.description = description;
        this.name = name;
        this.author = author;
        this.age = age;
        this.classification = classification;
    }
    getDescription() {
        return this.description
    }
    setDescription(description) {
        this.description = description
    }
    getName() {
        return this.name
    }
    setName(name) {
        this.name = name
    }
    getAuthor() {
        return this.author
    }
    setAuthor(author) {
        this.author = author
    }
    getAge() {
        return this.age
    }
    setAge(age) {
        this.age = age
    }

    getClassification() {
        return this.classification
    }
    setClassification(classification) {
        this.classification = classification;
    }

}